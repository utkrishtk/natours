exports.resizeTourImages = catchAsync(async (req, res, next) => {
  // console.log(req.files);
  if (!req.files.imageCover || !req.files.images) next();

  // 1)Cover image

  req.body.imageCover = `tour-${req.params.id}-${Date.now()}-cover.jpeg`; //we are putting imageCover on req.body so that it could pick up by next middleware while updating

  await sharp(req.files.imageCover[0].buffer)
    .resize(2000, 1333)
    .toFormat('jpeg')
    .jpeg({ quality: 90 })
    .toFile(`public/img/tours/${req.body.imageCover}`);

  // 2)Images
  req.body.images = [];

  await Promise.all(
    //Reason for using promise.all is that it will hold from calling next middleware
    req.files.images.map(async (file, i) => {
      const filename = `tour-${req.params.id}-${Date.now()}-${i + 1}.jpeg`;

      await sharp(file.buffer)
        .resize(2000, 1333)
        .toFormat('jpeg')
        .jpeg({ quality: 90 })
        .toFile(`public/img/tours/${filename}`);

      req.body.images.push(filename);
    })
  );

  next(); //The reason for making this function async is that we should only move next once the processing for image has done in each cases
});

